     document.querySelectorAll('.roadmap-step').forEach(step => {
        step.addEventListener('click', () => {
            step.scrollIntoView({ behavior: 'smooth' });
        });
    });

    // Animation on Scroll
    const steps = document.querySelectorAll('.roadmap-step');
    const line = document.getElementById('line');

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            } else {
                entry.target.classList.remove('visible');
            }
        });
    }, { threshold: 0.5 });

    steps.forEach(step => {
        observer.observe(step);
    });

    // Gradient Progress Effect on Line
    window.addEventListener('scroll', () => {
        const scrollTop = window.scrollY;
        const viewportHeight = window.innerHeight;
        const totalHeight = document.body.scrollHeight;
        const scrollPercentage = (scrollTop / (totalHeight - viewportHeight)) * 100;

        line.style.background = `linear-gradient(to bottom, #4CAF50 ${scrollPercentage}%, transparent ${scrollPercentage}%)`;
    });
</script>

